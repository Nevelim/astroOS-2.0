def remedies_for(element: Element) -> tuple[Remedy, ...]:
    """Return the catalog remedies for an element (REMED-2)."""
    return _CATALOG.get(element, ())


def build_reasoning(element: Element, day_master_element: Element,
                    lang: str = "en") -> str:
    """Privacy-safe reasoning: references the Day Master element, not birth data.

    Matches the API guide example shape:
      "Компенсирует слабый water по Day Master 甲 Wood"
    """
    el = element.value
    dm = day_master_element.value
    role = "nourishes" if element == favorable_elements(day_master_element)[0] \
        else "balances"
    if lang == "ru":
        return f"Компенсирует и {role} элемент {el} по Day Master ({dm})."
    return f"{role.capitalize()} your {dm} Day Master through the {el} element."
