"""Remedies catalog: favorable-element → remedy mapping (REMED-2).

Pure functions, no I/O. Two pieces:
  - favorable_elements(day_master_element) — the 用神 (yong shen) heuristic
    mirrored from bazi_engine: mother element (generates DM) + wealth element
    (DM controls). Advisory; a real reading overrides.
  - remedies_for(element) → the catalog of stones/colors/metals/scents that
    support a given element.

These mappings are the deterministic dev reference. Production keeps the same
shape; a curated catalog (DB-backed, curated by a practitioner) drops in
behind the same function signatures.

Privacy: reasoning strings reference ONLY the Day Master element, never birth
data (date/time/coords). They explain *why* an element is favorable.
"""
from __future__ import annotations

from services.remedies.domain.entities import (
    Element,
    Remedy,
    RemedyType,
)


# --------------------------------------------------------------------------- #
# Favorable elements (用神 heuristic) — mirrors bazi_engine.interpretation
# --------------------------------------------------------------------------- #
# Generating cycle: mother generates child. We invert it: who generates X?
_GENERATED_BY: dict[Element, Element] = {
    # X is generated by mother: mother → X
    Element.WOOD: Element.WATER,    # water generates wood
    Element.FIRE: Element.WOOD,     # wood generates fire
    Element.EARTH: Element.FIRE,    # fire generates earth
    Element.METAL: Element.EARTH,   # earth generates metal
    Element.WATER: Element.METAL,   # metal generates water
}
# Controlling cycle: X controls Y. Wealth = the element the Day Master controls.
_CONTROLS: dict[Element, Element] = {
    Element.WOOD: Element.EARTH,    # wood controls earth
    Element.FIRE: Element.METAL,    # fire controls metal
    Element.EARTH: Element.WATER,   # earth controls water
    Element.METAL: Element.WOOD,    # metal controls wood
    Element.WATER: Element.FIRE,    # water controls fire
}


def favorable_elements(day_master_element: Element) -> tuple[Element, ...]:
    """Advisory favorable elements (用神): mother (resource) + wealth.

    Mother = the element that GENERATES the Day Master (nourishes it).
    Wealth = the element the Day Master CONTROLS (its output/achievement).
    A real BaZi reading may override this heuristic.
    """
    mother = _GENERATED_BY[day_master_element]
    wealth = _CONTROLS[day_master_element]
    # De-dup while preserving order (a balanced DM could theoretically overlap).
    seen: list[Element] = []
    for el in (mother, wealth):
        if el not in seen:
            seen.append(el)
    return tuple(seen)


